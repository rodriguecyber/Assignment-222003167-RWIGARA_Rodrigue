

public class nestedfor {
    public static void main(String[] args)  {
        //decralation of variable
        int x;
        // start for loop
        for(x=2; x<11; x++)
        {
            //add nest for loop
        for(int y=3; y<=10;y++)
        {
System.out.print(x +" x " +y+" = ");
System.out.println(x*y);
        }
        System.out.println();
    }}
}
